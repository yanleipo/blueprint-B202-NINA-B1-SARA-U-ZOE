/**@brief Location and navigation time-out handler.
 *
 * @details This function will be called each time the location and navigation measurement timer expires.
 *
 * @param[in]   p_context   Pointer used for passing some arbitrary information (context) from the
 *                          app_start_timer() call to the time-out handler.
 */
static void loc_and_nav_timeout_handler(void * p_context)
{
    uint32_t err_code;

    UNUSED_PARAMETER(p_context);

    if(gnss_get_lns(&(m_location_speed.latitude), &(m_location_speed.longitude), &(m_location_speed.utc_time)))
    {
        err_code = ble_lns_loc_speed_send(&m_lns);
        if (err_code != NRF_ERROR_INVALID_STATE)
        {
            APP_ERROR_CHECK(err_code);
        }
    }
    
}

/**@brief Function for handling the data from the Nordic UART Service.
 *
 * @details This function will process the data received from the Nordic UART BLE Service and send
 *          it to the UART module.
 *
 * @param[in] p_evt       Nordic UART Service event.
 */
/**@snippet [Handling the data received over BLE] */
static void nus_data_handler(ble_nus_evt_t * p_evt)
{

    if (p_evt->type == BLE_NUS_EVT_RX_DATA)
    {
        uint32_t err_code;

        NRF_LOG_DEBUG("Received data from BLE NUS. Writing data on UART.");
        NRF_LOG_HEXDUMP_DEBUG(p_evt->params.rx_data.p_data, p_evt->params.rx_data.length);

        for (uint32_t i = 0; i < p_evt->params.rx_data.length; i++)
        {
            do
            {
                err_code = app_uart_put(p_evt->params.rx_data.p_data[i]);
                if ((err_code != NRF_SUCCESS) && (err_code != NRF_ERROR_BUSY))
                {
                    NRF_LOG_ERROR("Failed receiving NUS message. Error 0x%x. ", err_code);
                    APP_ERROR_CHECK(err_code);
                }
            } while (err_code == NRF_ERROR_BUSY);
        }
        if( p_evt->params.rx_data.length >= 9)
        {
            if( (p_evt->params.rx_data.p_data[0]=='d') &&\
                (p_evt->params.rx_data.p_data[1]=='b') &&\
                (p_evt->params.rx_data.p_data[2]=='g') &&\
                (p_evt->params.rx_data.p_data[3]=='-') &&\
                (p_evt->params.rx_data.p_data[4]=='c') &&\
                (p_evt->params.rx_data.p_data[5]=='e') &&\
                (p_evt->params.rx_data.p_data[6]=='l') &&\
                (p_evt->params.rx_data.p_data[7]=='='))
            {
                if(p_evt->params.rx_data.p_data[8]=='1')
                {
                    m_dbg_UART_passthrough = true;
                    NRF_LOG_INFO("Enabling UART passthrough.");
                }
                if(p_evt->params.rx_data.p_data[8]=='0')
                {
                    m_dbg_UART_passthrough = false;
                    NRF_LOG_INFO("Disabling UART passthrough.");
                }
            }
        }
        
        if( p_evt->params.rx_data.length >= 5)
        {
            if( (p_evt->params.rx_data.p_data[0]=='a') &&\
                (p_evt->params.rx_data.p_data[1]=='p') &&\
                (p_evt->params.rx_data.p_data[2]=='n') &&\
                (p_evt->params.rx_data.p_data[3]=='='))
            {
                uint8_t apn_length = p_evt->params.rx_data.length-4;
                uint8_t* apn = (uint8_t*)malloc(apn_length * sizeof(uint8_t*));
                memset(apn, 0, apn_length * sizeof(uint8_t*));
                memcpy(apn, &p_evt->params.rx_data.p_data[4], apn_length);
                NRF_LOG_INFO("APN set to %s.", apn);
                cellular_set_apn(apn);
                free(apn);
            }
        }

        if (p_evt->params.rx_data.p_data[p_evt->params.rx_data.length - 1] == '\r')
        {
            while (app_uart_put('\n') == NRF_ERROR_BUSY);
        }
    }

}

/**@brief   Function for handling app_uart events.
 *
 * @details This function will receive a single character from the app_uart module and append it to
 *          a string. The string will be be sent over BLE when the last character received was a
 *          'new line' '\n' (hex 0x0A) or if the string has reached the maximum data length.
 */
/**@snippet [Handling the data received over UART] */
void uart_event_handle(app_uart_evt_t * p_event)
{
    static uint8_t data_array[BLE_NUS_MAX_DATA_LEN];
    static uint8_t index = 0;
    uint32_t       err_code;

    switch (p_event->evt_type)
    {
        case APP_UART_DATA_READY:
            if(m_dbg_UART_passthrough)
            {
                UNUSED_VARIABLE(app_uart_get(&data_array[index]));
                index++;

                if ((data_array[index - 1] == '\n') ||
                    (data_array[index - 1] == '\r') ||
                    (index >= m_ble_nus_max_data_len))
                {
                    if (index > 1)
                    {
                        NRF_LOG_DEBUG("Ready to send data over BLE NUS");
                        NRF_LOG_HEXDUMP_DEBUG(data_array, index);

                        do
                        {
                            uint16_t length = (uint16_t)index;
                            err_code = ble_nus_data_send(&m_nus, data_array, &length, m_conn_handle);
                            if ((err_code != NRF_ERROR_INVALID_STATE) &&
                                (err_code != NRF_ERROR_RESOURCES) &&
                                (err_code != NRF_ERROR_NOT_FOUND))
                            {
                                APP_ERROR_CHECK(err_code);
                            }
                        } while (err_code == NRF_ERROR_RESOURCES);
                    }

                    index = 0;
                }
            }
            break;

        case APP_UART_COMMUNICATION_ERROR:
            APP_ERROR_HANDLER(p_event->data.error_communication);
            //NRF_LOG_ERROR("APP_UART_COMMUNICATION_ERROR");
            break;

        case APP_UART_FIFO_ERROR:
            APP_ERROR_HANDLER(p_event->data.error_code);
            //NRF_LOG_ERROR("APP_UART_FIFO_ERROR");
            break;

        default:
            break;
    }
}
